"""---

## Question 7 (3 points)

**This is the main challenge: Extract all dates from the medical notes and sort them chronologically.**

**Rules:**
- Assume all dates in `xx/xx/xx` format are `mm/dd/yy`
- Assume all 2-digit years are from the 1900s (e.g., `1/5/89` is January 5th, 1989)
- If the day is missing (e.g., `9/2009`), assume it is the 1st day of the month
- If the month is missing (e.g., `2010`), assume it is January 1st

*This function should return a pandas Series of length 500, where the values are the original indices sorted by date in ascending chronological order.*

**Example:**
```python
# If original series was:
#    0    1999
#    1    2010
#    2    1978
# Your function should return:
#    0    2    (1978 is earliest)
#    1    0    (1999 is second)
#    2    1    (2010 is latest)
```
"""

def question_seven():

    extracted = []

    for idx, text in enumerate(df): # Corrected from enumerate(df['text'])

        # Normalize text
        text_clean = text.lower()

        # Remove ordinal suffixes: 20th → 20
        text_clean = re.sub(r'(\d+)(st|nd|rd|th)', r'\1', text_clean)

        # Remove dots in months: Mar. → Mar
        text_clean = re.sub(r'\.', '', text_clean)

        # Patterns ordered from most specific → least specific
        patterns = [
            r'\b\d{1,2}/\d{1,2}/\d{2,4}\b',
            r'\b\d{1,2}-\d{1,2}-\d{2,4}\b',
            r'\b\w+\s\d{1,2},?\s\d{4}\b',
            r'\b\d{1,2}\s\w+,?\s\d{4}\b',
            r'\b\w+\s\d{4}\b',
            r'\b\d{1,2}/\d{4}\b',
            r'\b\d{4}\b'
        ]

        found = None

        for pattern in patterns:
            match = re.search(pattern, text_clean)
            if match:
                found = match.group()
                break

        # Convert to datetime
        date = pd.to_datetime(found, errors='coerce')

        extracted.append((idx, date))

    # Build dataframe
    df_dates = pd.DataFrame(extracted, columns=['idx', 'date'])

    # Sort by date
    df_sorted = df_dates.sort_values(by='date')

    return pd.Series(df_sorted['idx'].values)
