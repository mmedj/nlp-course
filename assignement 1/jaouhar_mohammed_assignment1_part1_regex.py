def question_six(text):
    """
    Extract phone numbers and return them in XXX-XXX-XXXX format.

    Args:
        text (str): Input text

    Returns:
        list: List of phone numbers in XXX-XXX-XXXX format
    """

    pattern = r'\b(?:\(?(\d{3})\)?[-.\s]?(\d{3})[-.\s]?(\d{4}))\b'
    matches = re.findall(pattern, text)

    standardized_numbers = []
    for match in matches:
        # Join the groups with hyphens to standardize
        standardized_numbers.append(f'{match[0]}-{match[1]}-{match[2]}')

    return standardized_numbers  # Return list of standardized phone numbers

